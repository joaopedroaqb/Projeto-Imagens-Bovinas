imagens_train = imagens[0:setenta_porcento]
imagens_test = imagens[setenta_porcento:(setenta_porcento+vinte_e_cinco_porcento)]
imagens_val = imagens[(setenta_porcento+vinte_e_cinco_porcento):]
source_dir = "imagens"

folder_name_train = "train"
folder_name_test = "test"
folder_name_val = "val"
os.makedirs(folder_name_train, exist_ok=True)
os.makedirs(folder_name_test, exist_ok=True)
os.makedirs(folder_name_val, exist_ok=True)

for pasta in listaPastas2:
    folder_name = f"{pasta['sigla']}"
